    modern. Saat ini dapat dijumpai berbagai motif untuk berbagi model
    bentuk barang. Yang paling banyak dijumpai barang bentuk Guci, Pot,
    Meja, Kursi, kursi, dan lain-lain. Namun sekarang sudah berkembang
