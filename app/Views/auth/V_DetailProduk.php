    Gerabah diperkirakan telah ada sejak masa pra sejarah, tepatnya
    setelah manusia hidup menetap dan mulai bercocok tanam. Situs-situs
    arkeologi di indonesia, telah ditemukan banyak tembikar yang
    berfungsi sebagai perkakas rumah tangga atau keperluan religius
    seperti upacara dan penguburan. Kasongan mulanya adalah tanah
    pesawahan milik penduduk desa di selatan Yogyakarta. Pada masa
    Penjajahan Belanda di Indonesia, di daerah pesawahan milik salah
    satu warga tersebut ditemukan seekor kuda yang mati. Kuda tersebut
    diperkirakan milik pejabat Belanda. Karena saat itu masa penjajahan
    Belanda, maka warga yang memiliki tanah tersebut takut dan segera
    melepaskan hak tanahnya supaya tidak dituntut oleh Belanda.
