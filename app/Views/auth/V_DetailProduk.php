      Rotan adalah sekelompok palma dari puak (tribus) Calameae yang
      memiliki habitus memanjat, terutama Calamus, Daemonorops, dan
      Oncocalamus. Puak Calameae sendiri terdiri dari sekitar enam ratus
      anggota, dengan daerah persebaran di bagian tropis Afrika, Asia dan
      Australasia. Ke dalam puak ini termasuk pula marga Salacca (
      misalnya salak), Metroxylon (misalnya rumbia/ sagu), serta Pigafetta
      yang tidak memanjat, dan secara tradisional tidak digolongkan
      sebagai tumbuhan rotan. Batang rotan biasanya langsing dengan
      diameter 2–5 cm, beruas-ruas panjang, tidak berongga, dan banyak
      yang dilindungi oleh duri-duri panjang, keras, dan tajam. Duri ini
      berfungsi sebagai alat pertahanan diri dari herbivora, sekaligus
      membantu pemanjatan, karena rotan tidak dilengkapi dengan sulur.
      Suatu batang rotan dapat mencapai panjang ratusan meter. Batang
      rotan mengeluarkan air jika ditebas dan dapat digunakan sebagai cara
      bertahan hidup di alam bebas. Badak jawa diketahui juga menjadikan
      rotan sebagai salah satu menunya.
